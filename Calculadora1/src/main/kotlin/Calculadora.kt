class Calculadora {

    companion object {

        fun multi(num: Int, numOpc: Int): Int {
            return num * numOpc
        }

        fun divi(num: Int, numOpc: Int): Int {
            return num / numOpc
        }

        fun soma(num: Int, numOpc: Int): Int {
            return num + numOpc
        }

        fun sub(num: Int, numOpc: Int): Int {
            return num - numOpc
        }

    }
}